        private void Guardar_SQL()
        {
            try
            {
                string rptaDatosBasicos = "";

                // <<<<<<------ Panel Datos Basicos ------>>>>>

                if (this.TBBodega.Text == Campo)
                {
                    MensajeError("Ingrese el nombre de la Bodega a registrar");
                }
                else if (this.TBDescripcion.Text == Campo)
                {
                    MensajeError("Ingrese la descripcion de la bodega a registrar");
                }
                else if (this.TBDirector.Text == Campo)
                {
                    MensajeError("Ingrese el nombre del Director o Responsable de la bodega");
                }
                else if (this.CBSucurzal.SelectedIndex == 0)
                {
                    MensajeError("Seleccione la sucurzal a la cual pernecera la bodega");
                }
                else
                {
                    if (this.Digitar)
                    {
                        rptaDatosBasicos = fBodega.Guardar_DatosBasicos

                            (
                                 //Datos Auxiliares
                                 1,

                                 //Panel Datos Basicos
                                 Convert.ToInt32(this.CBSucurzal.SelectedValue), this.TBBodega.Text, this.TBDescripcion.Text, this.TBDirector.Text, this.TBCiudad.Text, this.TBMovil.Text,
                                 this.TBTelefono.Text, this.TBCorreo.Text, this.TBDireccion01.Text, this.TBDireccion02.Text, this.TBRecepcion.Text, this.TBDespacho.Text, this.TBNumeroPC.Text,
                                 this.TBNumeroCelular.Text, this.TBNumeroImpresora.Text, this.TBOtrosEquipos.Text, this.TBInicioLaboral.Text,
                                 this.TBFinalHorarioLaboral.Text, this.TBDiadepagos.Text, this.TBMedidas.Text, 1
                            );
                    }

                    else
                    {
                        rptaDatosBasicos = fBodega.Editar_DatosBasicos

                            (
                                 //Datos Auxiliares
                                 2,

                                 //Panel Datos Basicos
                                 Convert.ToInt32(this.TBIdbodega.Text), Convert.ToInt32(this.CBSucurzal.SelectedValue), this.TBBodega.Text, this.TBDescripcion.Text, this.TBDirector.Text, this.TBCiudad.Text, this.TBMovil.Text,
                                 this.TBTelefono.Text, this.TBCorreo.Text, this.TBDireccion01.Text, this.TBDireccion02.Text, this.TBRecepcion.Text, this.TBDespacho.Text, this.TBNumeroPC.Text,
                                 this.TBNumeroCelular.Text, this.TBNumeroImpresora.Text, this.TBOtrosEquipos.Text, this.TBInicioLaboral.Text,
                                 this.TBFinalHorarioLaboral.Text, this.TBDiadepagos.Text, this.TBMedidas.Text, 1
                            );
                    }

                    if (rptaDatosBasicos.Equals("OK"))
                    {
                        if (this.Digitar)
                        {
                            this.MensajeOk("Registro Exitoso");
                        }

                        else
                        {
                            this.MensajeOk("Registro Actualizado");
                        }
                    }

                    else
                    {
                        this.MensajeError(rptaDatosBasicos);
                    }

                    //Llamada de Clase
                    this.Digitar = false;
                    this.Limpiar_Datos();
                }

            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.Message + ex.StackTrace);
            }
        }

